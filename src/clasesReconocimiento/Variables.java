package clasesReconocimiento;

public class Variables {

	public static void main(String[] args) {

		/*
		 * Tipos de datos primitivos
		 */
		
		// String
		String cadenaDeCaracteres = "Hola", x2 = "Mundo";
		System.out.println(cadenaDeCaracteres + " " + x2); //Concatenación de cadenas de carácteres

		// Int
		int numero1;
		numero1 = 9;
		System.out.println(numero1);

		// Double
		double double1 = 10 / 3;
		System.out.println(double1);

		// Float
		float float1 = 10.3f, xx2;
		System.out.println(float1);

		// Char
		char caracter = 's';
		System.out.println(caracter);

		// Booleano
		boolean booleano1 = true;
		System.out.println(booleano1);
		
		
	
		/*
		 * Operadores Aritméticos
		 */
		
		/*
		 + → Adición
		 - → Sustracción
		 * → Multiplicación
		 / → Divisón
		 % → Modulo
		 ++ → Incremento
		 -- → Decremento	 
		 */

		
		
		/*
		 * Operadores de Asignación
		 */
		
		/*
		 = → x=5;
		 += → x=x+6;
		 -= → x=x-7;
		 *= → x=x*8;
		 /= → x=x/5;
		 %= →x=x%2;
		 */
		
		/*
		 * Operadores de comparación
		 */
		
		/*
		 == → Igual a
		 != → Distinto a
		 > → Mayor que
		 < → Menor que
		 >= → Mayor o igual que
		 <= → Menor o igual que
		 */
		
		/*
		 * Operadores lógicos
		 */
		
		/*
		 && → AND
		 || → OR
		 ! → Not
		 */

	}

}
